<p>Get first hand information on all CDF progrmas,that the government has and is working on.<br /> <br/>
The Government of the Republic of Zambia has prioritised Decentralisation as a major driver for attaining development, reducing poverty and supporting job creation through citizen engagement and participation at the local level. To actualise decentralisation in line with the Constitution, Government has decided to take significant resources from the National to the Local Government level. In this regard, the Constituency Development Fund (CDF) has been identified as a major tool to take resources closer to the people. 

The CDF was first established in 1995 to support micro-community projects as part of the wider decentralization and local development policy. The fundamental objective was to provide resources to bridge the financing gap arising from non-existence sources of funds to finance micro-community led projects in all the Constituencies across the Country. The first appropriation and disbursement of this fund was done in 1995 by Parliament. From 1995 to 2016, the Fund was provided for under Section 45 of the Local Government Act Cap 281 of the Laws of Zambia. In 2016, the Constitution established the Constituency Development Fund. Subsequently, the Local Government Act was repealed and the Constituency Development Fund Act was enacted in 2018 to provide for the management, disbursement, utilisation and accountability of the Fund. Government developed the first guidelines in 2006 commonly known as the "CDF Guidelines to guide on the Management and Utilisation of the Fund". 
                                                  
The Fund has since evolved with an expanded scope and increased budget allocation with increased emphasis on enhanced community participation in determining local development priorities. In 2021, Government increased the amount of the CDF from K1.6 million to K25.7 million per Constituency. The expanded scope of the CDF covers three (3) specific areas namely; Community Projects; Youth, Women and Community Empowerment and Secondary Boarding School and Skills Development Bursaries. </p>
